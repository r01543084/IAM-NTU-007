function [u] = intpol(u)

a = 1/120*(-u(:,1,:,:)+5*u(:,2,:,:)-10*u(:,3,:,:)+10*u(:,4,:,:)-5*u(:,5,:,:) +u(:,end,:,:));

b = u(:,1,:,:)/8-(7*u(:,2,:,:))/12+(13*u(:,3,:,:))/12 -u(:,4,:,:)+(11*u(:,5,:,:))/24 -u(:,end,:,:)/12;

c = -((17*u(:,1,:,:))/24)+(71*u(:,2,:,:))/24-(59*u(:,3,:,:))/12+(49*u(:,4,:,:))/12-(41*u(:,5,:,:))/24+(7*u(:,end,:,:))/24;

d = (15*u(:,1,:,:))/8-(77*u(:,2,:,:))/12+(107*u(:,3,:,:))/12-(13*u(:,4,:,:))/2+(61*u(:,5,:,:))/24-(5*u(:,end,:,:))/12;

e = -((137*u(:,1,:,:))/60)+5*u(:,2,:,:)-5*u(:,3,:,:)+(10*u(:,4,:,:))/3-(5*u(:,5,:,:))/4 +u(:,end,:,:)/5;

f = @(x) a*x.^5+b*x.^4+c*x.^3+d*x.^2+e*x+u(:,1,:,:);

uR = [f(2.5) f(3.5) f(4.5)];
uL = [f(2.5) f(3.5) f(4.5)];

u = [uR;uL];